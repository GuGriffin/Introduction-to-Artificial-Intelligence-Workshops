strs = input("Please enter a string: ")  # Prompt the user to input a string and assign it to `strs`
str_len = len(strs)  # Get the length of the string
str_max = max(strs)  # Get the largest character in the string (based on ASCII values)
str_min = min(strs)  # Get the smallest character in the string (based on ASCII values)
print("The length of the string is:", str_len)  # Output the length of the string
print("The largest character in the string is:", str_max)  # Output the largest character
print("The smallest character in the string is:", str_min)  # Output the smallest character
