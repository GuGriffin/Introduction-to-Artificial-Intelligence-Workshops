new_str = 'This is an example of cutting'  # Create a string
# Split the string using a space as the delimiter
print(new_str.split())
# Output: ['This', 'is', 'an', 'example', 'of', 'cutting']
# Split the string using a space as the delimiter, limiting the split to 3 times
print(new_str.split(' ', 2))
# Output: ['This', 'is', 'an', 'example of cutting']
