animal = ['elephant', 'monkey', 'snake', 'tiger']  # Create a list called 'animal'
length = len(animal)  # Get the length of the list and store it in 'length'
i = 0  # Initialize a counter 'i' to 0

# Loop through the list as long as 'i' is less than 'length'
while i < length:
    print(animal[i])  # Print the element at index 'i' in the list
    i += 1  # Increment 'i' by 1 to move to the next element
