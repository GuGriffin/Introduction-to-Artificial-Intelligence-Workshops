# Input a string and assign it to the variable 'strs'
strs = input("Please enter a string: ")
# Calculate the length of the string and assign it to the variable 'str_len'
str_len = len(strs)
# Calculate the maximum character in the string (based on ASCII value) and assign it to 'str_max'
str_max = max(strs)
# Calculate the minimum character in the string (based on ASCII value) and assign it to 'str_min'
str_min = min(strs)
# Output the length of the string
print("The length of the string is:", str_len)
# Output the maximum character in the string
print("The largest character in the string is:", str_max)
# Output the minimum character in the string
print("The smallest character in the string is:", str_min)
