i = 1  # Create a variable `i` and assign it a value of 1
S = 0  # Create a variable `S` and assign it a value of 0

while i <= 100:  # Loop until `i` is greater than 100
    S += i  # Add the current value of `i` to `S` (sum up the numbers)
    i += 1  # Increment `i` by 1

print("S = 1 + 2 + 3 + ... + 100 =", S)  # Output the value of `S`
